function adjustPrices(price)
	return math.floor(price / 10)
end

pricesForDraw = {}

function formatNumber(amount, stepper)
	local left, center, right = string.match(math.floor(amount), "^([^%d]*%d)(%d*)(.-)$")
	return left .. string.reverse(string.gsub(string.reverse(center), "(%d%d%d)", "%1" .. (stepper or " "))) .. right
end

for mainType, price in pairs(prices) do
	if type(price) == "number" then
		prices[mainType] = adjustPrices(price)
		pricesForDraw[mainType] = formatNumber(prices[mainType])
	else
		prices[mainType] = {}
		pricesForDraw[mainType] = {}

		for subType, price in pairs(price) do
			if type(price) == "number" then
				prices[mainType][subType] = adjustPrices(price)
				pricesForDraw[mainType][subType] = formatNumber(prices[mainType][subType])
			else
				prices[mainType][subType] = {}

				for model, price in pairs(price) do
					prices[mainType][subType][model] = adjustPrices(price)
					pricesForDraw[mainType][subType][model] = formatNumber(prices[mainType][subType][model])
				end
			end
		end
	end
end

