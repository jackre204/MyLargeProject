				y = y + rotatedY

				local rotatedXX, rotatedXY = rotateAround(angle, x, y, x + box[4] - box[7], y + box[5] - box[8])
				local rotatedYX, rotatedYY = rotateAround(angle, x, y, x + box[1] - box[7], y + box[2] - box[8])
				
