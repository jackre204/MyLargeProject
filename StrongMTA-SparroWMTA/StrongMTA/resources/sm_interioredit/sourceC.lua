function stripAccents(text)
	local normalized = ""

	for character in string.gfind(text, "([%z\1-\127\194-\244][\128-\191]*)") do
		if tableAccents[character] then
			normalized = normalized .. tableAccents[character]
		else
			normalized = normalized .. character
