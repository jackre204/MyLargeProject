						menuOffset.wall[activeSubMenu] = menuOffset.wall[activeSubMenu] - 1

						if menuOffset.wall[activeSubMenu] < 0 then
							menuOffset.wall[activeSubMenu] = 0
						end
					elseif key == "mouse_wheel_down" then
						menuOffset.wall[activeSubMenu] = menuOffset.wall[activeSubMenu] + 1

