		local maxX = math.max(startCoordX, endCoordX)
		local minY = math.min(startCoordY, endCoordY)
		local maxY = math.max(startCoordY, endCoordY)

		for x = minX, maxX do
			for y = minY, maxY do
				loopFunction(tileToCoord(x, y))
			end
		end
	end
end

